"""## Preprocessing pipelines

The modeling pipeline uses `ColumnTransformer` and scikit-learn `Pipeline` objects so that preprocessing is fit independently inside each training fold. Numeric features are median-imputed and optionally scaled. Categorical handling is included for robustness, although this dataset currently contains no categorical predictor columns after removing the target and identifier.
"""

def make_preprocessor(feature_columns, scale_numeric=True):
    """Create a leakage-safe preprocessing transformer for selected columns."""
    feature_columns = list(feature_columns)
    numeric_cols = [c for c in feature_columns if pd.api.types.is_numeric_dtype(X[c])]
    categorical_cols = [c for c in feature_columns if c not in numeric_cols]

    transformers = []
    if numeric_cols:
        num_steps = [("imputer", SimpleImputer(strategy="median"))]
        if scale_numeric:
            num_steps.append(("scaler", StandardScaler()))
        transformers.append(("numeric", Pipeline(num_steps), numeric_cols))

    if categorical_cols:
        transformers.append((
            "categorical",
            Pipeline([
                ("imputer", SimpleImputer(strategy="most_frequent")),
                ("onehot", OneHotEncoder(handle_unknown="ignore", sparse_output=False)),
            ]),
            categorical_cols,
        ))

    return ColumnTransformer(transformers=transformers, remainder="drop", verbose_feature_names_out=False)

